# MỌI XÂU ĐỀU LỚN HƠN XÂU RỖNG

# Toán tử ==
# so sánh bằng
str1 = "Welcome 123"
str2 = "Welcome 123"
print(f"{str1} == {str2}: {str1 == str2}")

print()

# Toán tử !=
# so sánh khác
str3 = "welcome 123"
print(f"{str1} != {str3}: {str1 != str3}")

print()

# Toán tử >
# so sánh lớn hơn
str4 = "ac"
str5 = "ab"
print(f"{str4} > {str5}: {str4 > str5}")

print()

# Toán tử <
# so sánh nhỏ hơn
str6 = "2A"
str7 = "2a"
print(f"{str6} < {str7}: {str6 < str7}")

print()

# Toán tử >=
# so sánh lớn hơn hoặc bằng
print(f"{str4} >= {str5}: {str4 >= str5}")

print()

# Toán tử <=
# so sánh nhỏ hơn hoặc bằng
str6 = "2A"
str7 = "2a"
print(f"{str6} <= {str7}: {str6 <= str7}")